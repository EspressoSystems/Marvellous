def convert_bytes(input):
	res = []
	for v in input:
		res.append(int(v))
		
	res_str = "["
	
	for v in res:
		res_str +="0x%02X," % v
	
	res_str += "]"
	res_str = res_str.replace(",]", "]")
		
	return res_str
	
